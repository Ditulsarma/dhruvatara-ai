def get_text(key: str, lang: str = "as") -> str:
    """Get translated text for a given key and language.
    Falls back to Assamese (as) if translation not found.
    """
    if key not in TRANSLATIONS:
        return key  # Return the key itself if not found

    entry = TRANSLATIONS[key]
    if lang in entry:
        return entry[lang]

    # Fallback chain: lang -> as -> en -> key
    if "as" in entry:
        return entry["as"]
    if "en" in entry:
        return entry["en"]
    return key


def get_all_texts(lang: str = "as") -> dict:
    """Get all translations for a given language as a flat dict."""
    result = {}
    for key in TRANSLATIONS:
        result[key] = get_text(key, lang)
    return result
