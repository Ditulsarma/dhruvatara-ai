def get_tripap_rista(nak_index: int, lang: str = 'as') -> dict:
    """
    Get Tripap Rista data for a given nakshatra index (1-27).
    Args:
        nak_index: 1-27
        lang: language code (as/bn/hi/en)
    Returns a dict with all 15 rows, each containing 12 values.
    """
    if nak_index < 1 or nak_index > 27:
        return None

    data = TRIPAP_DATA.get(nak_index, {})
    # Get i18n row labels and planet map
    try:
        from prediction_i18n import (
            get_tripap_row_label_i18n, get_tripap_planet_map_i18n,
            get_nakshatra_name_i18n, convert_tripap_data_to_lang
        )
        # Include all i18n label keys (r1-r15 plus col_house, col_desc, row_label)
        all_label_keys = list(ROW_LABELS.keys()) + ["col_house", "col_desc", "row_label"]
        row_labels = {k: get_tripap_row_label_i18n(k, lang) for k in all_label_keys}
        planet_map = get_tripap_planet_map_i18n(lang)
        nakshatra_name = get_nakshatra_name_i18n(nak_index - 1, lang)  # 0-based index
        # Convert data values to English digits for hi/en languages (fix language mixing)
        data = convert_tripap_data_to_lang(data, lang)
    except ImportError:
        row_labels = dict(ROW_LABELS)
        planet_map = PLANET_MAP
        nakshatra_name = NAKSHATRA_NAMES_TRIPAP[nak_index]

    return {
        "nakshatra": nakshatra_name,
        "nakshatra_index": nak_index,
        "rows": data,
        "row_labels": row_labels,
        "row_categories": ROW_CATEGORY,
        "planet_map": planet_map
    }


def get_tripap_for_house(nak_index: int, house_index: int) -> dict:
    """
    Get Tripap Rista values for a specific house (0-11) in a given nakshatra.
    Returns values from all 15 rows for that house column.
    """
    if nak_index < 1 or nak_index > 27:
        return None
    if house_index < 0 or house_index > 11:
        return None

    data = TRIPAP_DATA.get(nak_index, {})
    result = {"nakshatra": NAKSHATRA_NAMES_TRIPAP[nak_index], "house": house_index + 1}

    for row_key in [f"r{i}" for i in range(1, 16)]:
        if row_key in data:
            val = data[row_key][house_index]
            cat = ROW_CATEGORY.get(row_key, "unknown")
            if cat == "planet" and val in PLANET_MAP:
                result[row_key] = {"value": val, "full_name": PLANET_MAP[val], "label": ROW_LABELS[row_key]}
            else:
                result[row_key] = {"value": val, "label": ROW_LABELS[row_key]}

    return result


def analyze_tripap_rista(nak_index: int, planet_houses: dict) -> dict:
    """
    Analyze Tripap Rista by matching planets in houses against the Tripap table.
    planet_houses: {"ৰবি": house_idx, "চন্দ্ৰ": house_idx, ...} (0-indexed)
    Returns affliction analysis.
    """
    if nak_index < 1 or nak_index > 27:
        return None

    data = TRIPAP_DATA.get(nak_index, {})
    planet_rows = ["r3", "r4", "r5", "r8", "r9", "r10", "r13", "r14", "r15"]

    afflictions = []
    for row_key in planet_rows:
        if row_key not in data:
            continue
        for house_idx, pap_abbr in enumerate(data[row_key]):
            pap_full = PLANET_MAP.get(pap_abbr, pap_abbr)
            # Check if this pap planet is actually placed in this house
            for planet_name, planet_house in planet_houses.items():
                if planet_name == pap_full and planet_house == house_idx:
                    afflictions.append({
                        "row": row_key,
                        "row_label": ROW_LABELS[row_key],
                        "house": house_idx + 1,
                        "pap_planet": pap_full,
                        "actual_planet": planet_name,
                        "match": True
                    })

    return {
        "nakshatra": NAKSHATRA_NAMES_TRIPAP[nak_index],
        "nakshatra_index": nak_index,
        "total_afflictions": len(afflictions),
        "afflictions": afflictions,
        "severity": "তীব্ৰ" if len(afflictions) >= 5 else ("মধ্যম" if len(afflictions) >= 3 else ("সামান্য" if len(afflictions) >= 1 else "অনুপস্থিত"))
    }
